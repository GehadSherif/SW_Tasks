    private void Update_recordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Update_recordActionPerformed

        int ProductID = Integer.parseInt(this.jTextField1.getText());
        int NewProductID = Integer.parseInt(this.jTextField2.getText());
        String Type = this.jTextField3.getText();
        String Manufacturer = this.jTextField4.getText();
        String productionDate = this.jTextField5.getText();
        String expiryDate = this.jTextField6.getText();

        Product product = new Product();
        product.setProductID(ProductID);

        Product Newproduct = new Product();
        Newproduct.setProductID(NewProductID);
        Newproduct.setType(Type);
        Newproduct.setManufacturer(Manufacturer);
        Newproduct.setProductionDate(productionDate);
        Newproduct.setExpiryDate(expiryDate);

        ProductDao dao = new ProductDao();
        int result = dao.UpdateProduct(product, Newproduct);
        if (result != 0) {
            JOptionPane.showMessageDialog(null, "Product Updated!");

        } else {
            JOptionPane.showMessageDialog(null, "Failed");

        }
    }//GEN-LAST:event_Update_recordActionPerformed

