    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed

        int ProductID = Integer.parseInt(this.textField1.getText());
        String Type = this.textField2.getText();
        String Manufacturer = this.textField3.getText();
        String productionDate = this.textField4.getText();
        String expiryDate = this.textField5.getText();

        Product product = new Product();
        product.setProductID(ProductID);
        product.setType(Type);
        product.setManufacturer(Manufacturer);
        product.setProductionDate(productionDate);
        product.setExpiryDate(expiryDate);

        ProductDao dao = new ProductDao();
        int result = dao.InsertProduct(product);
        if (result != 0) {
            JOptionPane.showMessageDialog(null, "Product Added!");

        } else {
            JOptionPane.showMessageDialog(null, "Failed");

        }

    }//GEN-LAST:event_SubmitActionPerformed

